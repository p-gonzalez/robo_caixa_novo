package br.com.indra.entity;

public class CpfCnpj {
	
	private int idCpfCnpj;
	private String callId;
	private String valor_dado;
	private int  codDado;
	
	
	public int getIdCpfCnpj() {
		return idCpfCnpj;
	}
	public void setIdCpfCnpj(int idCpfCnpj) {
		this.idCpfCnpj = idCpfCnpj;
	}
	public String getCallId() {
		return callId;
	}
	public void setCallId(String callId) {
		this.callId = callId;
	}
	public String getValor_dado() {
		return valor_dado;
	}
	public void setValor_dado(String valor_dado) {
		this.valor_dado = valor_dado;
	}
	public int getCodDado() {
		return codDado;
	}
	public void setCodDado(int codDado) {
		this.codDado = codDado;
	}
	
	
	
	

}
